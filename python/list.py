# 리스트에서 값 수정하기
a = [1,2,3]
a[2] = 4
# 1,2,4 출력
print(a)

# del 함수 사용해 리스트 요소 삭제하기
a = [1,2,3]
del a[1]
#  1,3 출력
print(a)
# del a[x]는 x번째 요소를 삭제한다

# del 객체 사용법
