# 리스트는 []안에 , 로 구분한다.
a = []
b = [1,2,3]
c = ['life','is','too','short']
d = [1,2,'life','is']
e = [1,2,['life','is']]
# 리스트는 a 처럼 아무것도 포함하지 않아 비어 있는 리스트 일수도 있고
#  b 처럼 숫자를 요솟값으로 가질수 있으며
# c 처럼 문자를 요솟 값으로 가질수 있고
# d 처럼 숫자와 문자를 요소 값으로 함께 가질수도 있다.
# e 처럼 리스트를 요솟 값으로 가질수도 있다.
# 비어있는 리스트는 a = list() 로 생성할수 있다.

#리스트의 인덱싱과 슬라이싱
a = [1,2,3]
print(a)
print(a[0] + a[2])
print(a[-1])

# 리스트 a 에 숫자 123 과 또다른 리스트 인 abc를 포함시키기
a = [1,2,3,['a','b','c']]
# abc 리스트 출력
print(a[-1]) 
#  a 출력
print(a[-1][0])

#  삼중 리스트에서 인덱싱하기
a = [1,2,['a','b',['Life','is']]]
# Life
print(a[-1][-1][0])
print(a[2][2][0])

# 리스트의 슬라이싱
a = [1,2,3,4,5]
# 1,2 출력
print(a[0:2])
print(a[:2])
# 3,4,5 출력
print(a[2:])

# 리스트가 포함된 중첩리스트 슬라이싱
a = [1,2,3,['a','b','c'],4,5]
# 3,['a','b','c'],4 출력
print(a[2:5])
# a, b 출력
print(a[3][2])

# 리스트 연산
a = [1,2,3]
b = [4,5,6]
# 1,2,3,4,5,6 출력
print(a + b)

# 리스트 반복하기
a = [1,2,3]
# 1,2,3,1,2,3,1,2,3 출력
print(a * 3)

#  리스트 길이 구하기
a = [1,2,3]
print(len(a))
# len 함수는 문자열, 리스트, 튜플, 딕셔너리에도 사용가능한 함수

